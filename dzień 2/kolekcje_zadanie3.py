lista = [10, 20, 30]

i = 0
while i < len(lista):
    print(lista[i])
    i += 1

  # Pętla 'for' składa się z for zmienną pomocniczą el w liście
for el in lista:
    print(el)

    # range wykonywanie czynnosci w pętli ilość razy
    # w przykładzie od 2 do 50 co 2 liczby
for i in range(2, 50, 2):
    print(i)
